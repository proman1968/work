function modePromptBlock(mode = 'USER') {
    const m = String(mode || 'USER').toUpperCase();
    if (m === 'ADMIN') {
        return `

## Режим ADMIN
Ты развиваешь платформу WORK: код, handlers, $class, инфраструктура.
Приоритет — точные изменения системы, аккуратность с системными ($…) элементами.`;
    }
    if (m === 'BOSS') {
        return `

## Режим BOSS
Ты планируешь и запускаешь бизнес, проекты и процессы.
Приоритет — планы, решения, делегирование; формы — только для чисел/сроков/параметров запуска.`;
    }
    return `

## Режим USER
Ты выполняешь рутинные задачи в потоке уже запущенных бизнес-процессов.
Приоритет — быстрое исполнение шагов плана; минимум форм.`;
}

export default {
    label: 'on_save (.ai)',
    icon: 'carbon:ai',
    async execute(params = {}) {
        const taskFile = params.$context;
        if (!taskFile) return;

        let body;
        try {
            const raw = await taskFile.load({ encoding: 'utf-8' });
            body = typeof raw === 'string' ? JSON.parse(raw) : raw;
        } catch { return; }

        if (!body) return;

        body.ribbon ??= [];
        body.mode ??= 'USER';
        // Уже отвечали — type:text (новый) или role:assistant (legacy)
        const hasAssistant = body.ribbon.some(m =>
            m.type === 'text' || m.type === 'reasoning' || m.role === 'assistant');
        if (hasAssistant) return;

        let firstPrompt = '';
        const firstUser = body.ribbon.find(m => m.type === 'prompt' || m.role === 'user');
        if (firstUser) {
            firstPrompt = String(firstUser.content ?? '').trim();
            // Нормализация legacy role:user → type:prompt
            if (firstUser.role === 'user' && firstUser.type !== 'prompt') {
                firstUser.type = 'prompt';
                delete firstUser.role;
            }
        } else {
            firstPrompt = String(body?.title ?? '').trim();
        }
        if (!firstPrompt) return;

        if (!body.system) {
            body.system = SYSTEM_PROMPT + modePromptBlock(body.mode);
            const { pathToFileURL } = await import('node:url');
            const { join } = await import('node:path');
            const { findFirstModel } = await import(pathToFileURL(join(process.cwd(), 'sources/modules/ai-schema.js')).href);
            body.model = await findFirstModel();
        }

        // Сохранить system/model и нормализованный prompt
        try {
            const fsp = await import('node:fs/promises');
            await fsp.writeFile('.' + taskFile.path, JSON.stringify(body, null, 4), 'utf-8');
        } catch {}

        const methods = await taskFile._methods;
        const prompt = methods?.prompt;
        if (typeof prompt?.execute === 'function') {
            prompt.execute({ text: firstPrompt, user: params.user, $context: taskFile }).catch(e => {
                console.warn('[ai] prompt error:', e.message);
            });
        }
    },
};