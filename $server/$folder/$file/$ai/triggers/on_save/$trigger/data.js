export default {
    label: 'on_save (.ai)',
    icon: 'carbon:ai',
    async execute(params = {}) {
        // this — триггер on_save
        // params.$context — history-файл (.task.ai/h)
        const taskFile = params.$context;
        if (!taskFile) return;

        // Загружаем тело task.ai
        let body;
        try {
            const raw = await taskFile.load({ encoding: 'utf-8' });
            body = typeof raw === 'string' ? JSON.parse(raw) : raw;
        } catch { return; }

        if (!body) return;

        // Если уже есть ответ assistant — не запускаем повторно
        const hasAssistant = body?.chat?.some?.(m => m.role === 'assistant');
        if (hasAssistant) return;

        // Извлекаем первый запрос пользователя
        let firstPrompt = '';
        if (body?.chat?.[0]?.role === 'user') {
            firstPrompt = String(body.chat[0].content ?? '').trim();
        } else {
            firstPrompt = String(body?.chat?.[0]?.prompt ?? body?.title ?? '').trim();
        }
        if (!firstPrompt) return;

        // Добавляем системный промпт, модель, очищаем chat (prompt добавит сообщение сам)
        if (!body.system) {
            body.system = SYSTEM_PROMPT;
            body.chat = [];
            body.model = await findModel();
            try {
                const fsp = await import('node:fs/promises');
                await fsp.writeFile('.' + taskFile.path, JSON.stringify(body, null, 4), 'utf-8');
            } catch {}
        }

        // Запускаем метод prompt этого же файла
        const methods = await taskFile._methods;
        const prompt = methods?.prompt;
        if (typeof prompt?.execute === 'function') {
            prompt.execute({ text: firstPrompt, user: params.user, $context: taskFile }).catch(e => {
                console.warn('[ai] prompt error:', e.message);
            });
        }
    },
};

async function findModel() {
    try {
        const children = await WORK.children;
        const aiRoot = children?.find(el => el.type === '$ai');
        if (!aiRoot) return null;
        const tree = await aiRoot.info({ deep: -1 });
        return findFirstLeaf(tree)?.path || null;
    } catch (e) {
        console.warn('[ai] findModel:', e.message);
    }
    return null;
}

function findFirstLeaf(node) {
    if (!node) return null;
    const items = node.items;
    if (!items?.length) return node;
    return findFirstLeaf(items[0]);
}